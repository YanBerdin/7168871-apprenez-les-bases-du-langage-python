# Définissez une variable `nom` contenant votre **nom** sous forme de chaîne de caractères.
nom = "Dupont"
# Définissez une variable `age` contenant votre **âge** sous forme d'un entier.
age = 25
# Définissez une variable `taille` contenant votre **taille** sous forme d'un nombre à virgule flottante.
taille = 1.75
# Définissez une variable `est_etudiant` contenant une valeur booléenne et indiquant si vous êtes **étudiant ou non**.
est_etudiant = True
# Utilisez la fonction `print` pour afficher les valeurs des variables précédemment définies.
print("valeur de la variable nom :", nom)
print("valeur de la variable age :", age)
print("valeur de la variable taille :", taille)
print("valeur de la variable est_etudiant :", est_etudiant)
# Utilisez la fonction `type` pour afficher le type de chaque variable.
print("type de la variable nom :", type(nom))
print("type de la variable age :", type(age))
print("type de la variable taille :", type(taille))
print("type de la variable est_etudiant :", type(est_etudiant))
